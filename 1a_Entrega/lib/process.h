/* ===========================================
 * Process structure
 * Keeps process ID, status and exec time
 * ===========================================
 */

#ifndef __PROCESS__
#define __PROCESS__

typedef struct _process {
    int pid;
    int status;
} process;

/* =============================================================================
 * process_alloc
 * =============================================================================
 */
process* 
process_alloc(int pid, int status);


/* =============================================================================
 * process_free
 * =============================================================================
 */
void 
process_free(process* proc);


/* =============================================================================
 * process_print
 * =============================================================================
 */
void 
process_print(process* proc);


#endif //__PROCESS__